// for é utilizado quando você sabe a quantidade de repetições
for(let i = 0; i <= 10; i = i + 1){
    console.log("for", i);
}

//while é uma estrutura usada geralmente quando não sabemos quantas repetições vão acontecer
let j = 0;
    while(j <= 10) {
    console.log("while", j)
    j++
}

//É parecido com while, a diferença é que a PRIMEIRA estrução sempre irá acontecer
let k = 0;
var g = 2;
   do {
        console.log("DO_while", k);
        k++
   }while(k !== g);

